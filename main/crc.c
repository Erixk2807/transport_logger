unsigned short generate_crc_value (unsigned short lastCrc, unsigned long sizeOfBuffer, const char *buffer) {
    unsigned long i;
    unsigned char index;
    for (i = 0L; i < sizeOfBuffer; i++) {
        index = buffer[i] ^ ((char)(lastCrc & 0x00FF));
        lastCrc = ((lastCrc >> 8) & 0x00FF) ^ (crcTable[index]);
    }
    return (lastCrc);
}